# 밑변이 3이고 높이가 2인 삼각형의 넓이를 계산하라.
# 밑변과 높이를 변수에 할당
base_triangle = 3
height_triangle = 2

# 삼각형의 넓이 공식을 사용하여 넓이 계산
# 공식: (밑변 * 높이) / 2
area_triangle = (base_triangle * height_triangle) / 2

# 계산된 넓이 출력
print("삼각형의 넓이:", area_triangle)

print("========================================================================\n")

# 밑변이 5, 윗변이 3, 높이가 2인 사다리꼴의 넓이를 계산하라.
# 밑변, 윗변, 높이를 변수에 할당
base1_trapezoid = 5
base2_trapezoid = 3
height_trapezoid = 2

# 사다리꼴의 넓이 공식을 사용하여 넓이 계산
# 공식: ((밑변1 + 밑변2) * 높이) / 2
area_trapezoid = ((base1_trapezoid + base2_trapezoid) * height_trapezoid) / 2

# 계산된 넓이 출력
print("사다리꼴의 넓이:", area_trapezoid)
