# 산술 연산자 예제

# 두 수를 정의합니다.
a = 15
b = 4

# 덧셈
addition = a + b
print(f"{a} + {b} = {addition}")  # 15 + 4 = 19

# 뺄셈
subtraction = a - b
print(f"{a} - {b} = {subtraction}")  # 15 - 4 = 11

# 곱셈
multiplication = a * b
print(f"{a} * {b} = {multiplication}")  # 15 * 4 = 60

# 나눗셈
division = a / b
print(f"{a} / {b} = {division}")  # 15 / 4 = 3.75
# 나눗셈의 결과는 항상 실수입니다.

# 정수 나눗셈
integer_division = a // b
print(f"{a} // {b} = {integer_division}")  # 15 // 4 = 3
# 정수 나눗셈의 결과는 나눗셈의 몫만을 반환합니다.

# 나머지 연산
remainder = a % b
print(f"{a} % {b} = {remainder}")  # 15 % 4 = 3
# 나머지 연산은 나눗셈 후의 나머지를 반환합니다.

# 거듭제곱
exponentiation = a ** b
print(f"{a} ** {b} = {exponentiation}")  # 15 ** 4 = 50625
# a를 b번 곱한 값입니다.
